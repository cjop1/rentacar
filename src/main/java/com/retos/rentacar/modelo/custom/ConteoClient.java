package com.retos.rentacar.modelo.custom;
import com.retos.rentacar.modelo.Client;

/**
 *
 * @author cjop1
 */
public class ConteoClient {
    
    private Long total; //cantidad de reservas por cliente
    private Client client; //cliente

    public ConteoClient(Long total, Client client) {
        this.total = total;
        this.client = client;
    }

    public Long getTotal() {
        return total;
    }

    public void setTotal(Long total) {
        this.total = total;
    }

    public Client getClient() {
        return client;
    }

    public void setClient(Client client) {
        this.client = client;
    }

    
}
